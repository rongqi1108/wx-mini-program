package com.rong.pojo;

public class SearchRecords {

    private String id;

    /**
     * 搜索的内容
     */
    private String content;




    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getContent() {
        return content;
    }
    public void setContent(String content) {
        this.content = content;
    }
}